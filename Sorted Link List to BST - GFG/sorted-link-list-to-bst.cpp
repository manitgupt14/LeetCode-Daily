class Solution{
  public:
    TNode* sortedListToBST(LNode *head,LNode* end=NULL) {
        LNode *fast,*slow;
        fast=slow=head;
        if(head==end) return NULL;
        while(fast!=end and fast->next!=end)
        {
            slow=slow->next;
            fast=fast->next->next;
        }
        if(slow==NULL) return NULL;
        TNode *tmp=new TNode(slow->data);
        tmp->left=sortedListToBST(head,slow);
        tmp->right=sortedListToBST(slow->next,end);
        return tmp;
    }
};

